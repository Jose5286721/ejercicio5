    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        int index = Integer.parseInt(JOptionPane.showInputDialog("Introduzca el indice donde quiere insertar"));
        while(index<1 || medicamentos.tamano() < index){
            index = Integer.parseInt(JOptionPane.showInputDialog("Introduzca de vuelta el indice donde quiere insertar"));
        }
        Medicamento medicamento = new Medicamento();
        String marca = JOptionPane.showInputDialog("Introduzca la marca del medicamento");
        while(marca.isEmpty()){
            marca = JOptionPane.showInputDialog("Introduzca de vuelta la marca del medicamento");
        }
        String modelo = JOptionPane.showInputDialog("Introduzca el modelo del medicamento");
        while(modelo.isEmpty()){
            modelo = JOptionPane.showInputDialog("Introduzca de nuevo el modelo del medicamento");
        }
        Integer cantidad = Integer.parseInt(JOptionPane.showInputDialog("Introduzca la cantidad del medicamento"));
        while(cantidad < 1){
            cantidad = Integer.parseInt(JOptionPane.showInputDialog("Introduzca de nuevo la cantidad del medicamento"));
        }
        String fecha = JOptionPane.showInputDialog("Introduzca la fecha de entrada del medicamento");
        while(fecha.isEmpty()){
            fecha = JOptionPane.showInputDialog("Introduzca de nuevo la fecha de entrada del medicamento");
        }
        medicamento.setCantidad(cantidad);
        medicamento.setFecha(fecha);
        medicamento.setId("00"+index);
        medicamento.setModelo(modelo);
        medicamento.setMarca(marca);
        medicamentos.insertar(medicamento, index);
    }//GEN-LAST:event_jMenuItem5ActionPerformed

