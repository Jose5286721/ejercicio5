    private static void inicializarMedicamentos(){
        medicamentos = new ListaEstatica<Medicamento>();
        medicamentos.iniciar(countMedicamentos);
    }
    
    /**
     * AQUI LEEMOS EL ARCHIVO Y INICIALIZAMOS LAS VARIABLES DEL VECTOR
     */
    private static void leerArchivoMedicamentos(){
        try{
            fileReader = new FileReader("Medicamentos.txt");
            bufferedReader = new BufferedReader(fileReader);
            String lineaMedicamento = "";
            List<Medicamento> auxMedicamento = new ArrayList<Medicamento>();
            while(lineaMedicamento != null){
                lineaMedicamento = bufferedReader.readLine();
                if(lineaMedicamento != null){
                    auxMedicamento.add(toMedicamento(lineaMedicamento));
                }
            }
            countMedicamentos = auxMedicamento.size();
            inicializarMedicamentos();
            for(Medicamento medicamento: auxMedicamento){
                medicamentos.insertar(medicamento);
            }
            JOptionPane.showMessageDialog(null, "Se cargo los medicamentos");
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
    
    
    
    /**
     * LEEMOS LA CADENA DEL MEDICAMENTO Y RETORNAMOS EL MEDICAMENTO
     * @param medicamentoCrudo
     * @return 
     */
    private static Medicamento toMedicamento(String medicamentoCrudo){
        StringTokenizer stringTokenizer = new StringTokenizer(medicamentoCrudo);
        Medicamento medicamento = new Medicamento();
        int cont = 0;
        String dato = "";
        while(stringTokenizer.hasMoreTokens()){
            cont++;
            dato = stringTokenizer.nextToken();
            switch(cont){
                case 1:{
                    medicamento.setId(dato);
                    break;
                }
                case 2:{
                    medicamento.setMarca(dato);
                    break;
                }
                case 3:{
                    medicamento.setModelo(dato);
                    break;
                }
                case 4:{
                    medicamento.setCantidad(Integer.valueOf(dato));
                    break;
                }
                case 5:{
                    medicamento.setFecha(dato);
                }
            }
        }
        return medicamento;
    }
    
    
